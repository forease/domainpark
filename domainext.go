var domainext map[string]uint

func getDomainRoot( d string ) ( string, uint ) {
    var retRoot, tar string
    var dlong, djump, tmptopid, topid uint
    var ok bool

    dm := strings.Split( d, "." )
    // dl := len(dm)

    for i := len(dm) - 1; i >= 0; i-- {
        //fmt.Printf("%d, %s[%s]\n", i, dm[i], retRoot )
        retRoot = dm[i] + tar + retRoot
        tar = "."
        ok = false

        // 判断当前域名是否为根域名
        //fmt.Println("retRoot: ", retRoot )
        tmptopid, ok = domainext[ retRoot ]
        if ok {
            topid = tmptopid
            //fmt.Printf("%d: %s\n", topid, retRoot)
            if retRoot == "in-addr.arpa" {
                dlong = 2
            } else {
                dlong = 1
            }
            djump = 0
        }
        if dlong > 0 {
            if djump >= dlong {
                break
            }
            djump++
        }

    }
    //fmt.Printf("retRoot: %s, dlong: %d, dj: %d rootid %d\n", retRoot, dlong, djump, topid )
    return retRoot, topid
}


